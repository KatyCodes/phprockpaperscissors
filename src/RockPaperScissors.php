<?php

    class RockPaperScissors
    {

        function playGame($player1, $player2)
        {
            if ($player1 = "Rock" && $player2 == "Scissors") {
                return "Player 1";
            } elseif ($player1 = "Scissors" && $player2 == "Rock") {
                return "Player 2";
            }

        }

    }

?>
